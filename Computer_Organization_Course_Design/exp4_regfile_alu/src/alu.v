`timescale 1ns / 1ps

//==============================================================================
// alu - 32位算术逻辑单元 (32-bit ALU)
//==============================================================================
// 功能描述:
//   - 支持9种运算操作。
//   - 运算与操作码(alu_op)对应:
//       0000: a + b  (加法, 带进位cf和溢出of标志)
//       0001: a - b  (减法, 带借位cf和溢出of标志)
//       0010: a & b  (按位与)
//       0011: a | b  (按位或)
//       0100: a ^ b  (按位异或)
//       0101: a << 1 (逻辑左移1位)
//       0110: a >> 1 (逻辑右移1位)
//       0111: ~a     (按位取反)
//       default: 0
//   - 标志位: zf(零), cf(进位/借位), of(溢出), sf(符号)
//==============================================================================

module alu(
    input  wire [3:0]  alu_op,
    input  wire [31:0] a,
    input  wire [31:0] b,
    output reg  [31:0] f,
    output wire        zf,
    output wire        cf,
    output wire        of,
    output wire        sf
);

    reg c;                  // 进位/借位标志
    reg o;                  // 溢出标志
    reg [32:0] add_result;  // 33位加法结果 (含进位)

    // ALU 核心运算逻辑
    always @(*) begin
        f = 32'h00000000;
        c = 1'b0;
        o = 1'b0;
        add_result = 33'h000000000;

        case (alu_op)
            4'b0000: begin                     // 加法
                add_result = {1'b0, a} + {1'b0, b};
                f = add_result[31:0];
                c = add_result[32];            // 进位标志
                o = (~(a[31] ^ b[31])) & (f[31] ^ a[31]); // 溢出: 同号加得异号
            end
            4'b0001: begin                     // 减法
                f = a - b;
                c = (a < b);                   // 借位标志 (无符号比较)
                o = (a[31] ^ b[31]) & (f[31] ^ a[31]);    // 溢出: 异号减得异号
            end
            4'b0010: f = a & b;   // 按位与
            4'b0011: f = a | b;   // 按位或
            4'b0100: f = a ^ b;   // 按位异或
            4'b0101: f = a << 1;  // 逻辑左移1位
            4'b0110: f = a >> 1;  // 逻辑右移1位
            4'b0111: f = ~a;      // 按位取反
            default: f = 32'h00000000;
        endcase
    end

    // 标志位输出
    assign zf = (f == 32'h00000000);  // 零标志
    assign cf = c;                     // 进位/借位标志
    assign of = o;                     // 溢出标志
    assign sf = f[31];                 // 符号标志 (最高位)

endmodule
