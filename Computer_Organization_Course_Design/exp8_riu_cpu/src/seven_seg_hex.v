`timescale 1ns / 1ps

//==============================================================================
// seven_seg_hex - 16进制到七段数码管段码转换 (Hex to 7-Segment Decoder)
//==============================================================================
// 功能描述:
//   - 将4位16进制值转换为共阳极七段数码管的段选信号。
//   - 段码映射: seg[6:0] = {G, F, E, D, C, B, A} (低电平点亮)。
//   - 支持0-F的显示，无效输入时全部熄灭。
//==============================================================================

module seven_seg_hex(
    input  wire [3:0] hex,
    output reg  [6:0] seg
);

    always @(*) begin
        case (hex)
            4'h0: seg = 7'b1000000;  // 0: A,B,C,D,E,F 亮
            4'h1: seg = 7'b1111001;  // 1: B,C 亮
            4'h2: seg = 7'b0100100;  // 2: A,B,D,E,G 亮
            4'h3: seg = 7'b0110000;  // 3: A,B,C,D,G 亮
            4'h4: seg = 7'b0011001;  // 4: B,C,F,G 亮
            4'h5: seg = 7'b0010010;  // 5: A,C,D,F,G 亮
            4'h6: seg = 7'b0000010;  // 6: A,C,D,E,F,G 亮
            4'h7: seg = 7'b1111000;  // 7: A,B,C 亮
            4'h8: seg = 7'b0000000;  // 8: 全部亮
            4'h9: seg = 7'b0010000;  // 9: A,B,C,D,F,G 亮
            4'hA: seg = 7'b0001000;  // A: A,B,C,E,F,G 亮
            4'hB: seg = 7'b0000011;  // B: C,D,E,F,G 亮
            4'hC: seg = 7'b1000110;  // C: A,D,E,F 亮
            4'hD: seg = 7'b0100001;  // D: B,C,D,E,G 亮
            4'hE: seg = 7'b0000110;  // E: A,D,E,F,G 亮
            4'hF: seg = 7'b0001110;  // F: A,E,F,G 亮
            default: seg = 7'b1111111;  // 全部熄灭
        endcase
    end

endmodule
